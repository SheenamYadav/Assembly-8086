.MODEL SMALL
.STACK
.DATA
ARR DW 0111H,0112H,0113H,0114H,0115H
LEN DW ($-ARR)/2
KEY EQU 0115H
MSG1 DB "KEY IS FOUND AT "
RES DB " POSITION",13,10,"$"
MSG2 DB 'KEY NOT FOUND!!!!!!!!!!!!!. $'
CODE SEGMENT
MOV AX,@DATA
MOV DS,AX
MOV BX,00
MOV DX,LEN
MOV CX,KEY

AGAIN:CMP BX,DX
JA FAIL
MOV AX,BX
ADD AX,DX
SHR AX,1
MOV SI,AX
ADD SI,SI
CMP CX,ARR[SI]
JAE BIG
DEC AX
MOV DX,AX
JMP AGAIN

BIG:JE SUCCESS
INC AX
MOV BX,AX
JMP AGAIN

SUCCESS:ADD AL,01
ADD AL,'0'
MOV RES,AL
LEA DX,MSG1
JMP DISP

FAIL:LEA DX,MSG2

DISP:MOV AH,09H
INT 21H
MOV AH,4CH 